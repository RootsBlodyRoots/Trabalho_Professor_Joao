    private void popularTabela() {
        ColaboradorDao cd = new ColaboradorDao();
        List<CadastrarUsuario> listaColaborador;

        try {
            listaColaborador = cd.listarColaboradores();

            DefaultTableModel model = (DefaultTableModel) tblColaborador.getModel();
            List<Object> lista = new ArrayList<Object>();

            for (int i = 0; i < listaColaborador.size(); i++) {
                CadastrarUsuario c = listaColaborador.get(i);
                lista.add(new Object[]{c.getColaborador(), c.getTipo(), c.getUsuario(), c.getSenha(), 
                    c.getNome(), c.getSobrenome(), c.getEmail(), c.getEquipe()});
            }

            for (int idx = 0; idx < lista.size(); idx++) {
                model.addRow((Object[]) lista.get(idx));
            }
        } catch (SQLException ex) {
            String msg = "Ocorreu um erro ao obter os clientes do banco de dados!";
            JOptionPane.showMessageDialog(null, msg);
            Logger.getLogger(ListarColaboradorJPanel.class.getName()).log(Level.SEVERE, null, ex);
        }

    }

